/// Text variants for MText component
enum MTTextVariant {
  /// Regular body text
  body,

  /// Heading 1 - largest heading
  h1,

  /// Heading 2 - second level heading
  h2,

  /// Heading 3 - third level heading
  h3,

  /// Heading 4 - fourth level heading
  h4,

  /// Small text for captions and secondary information
  small,

  /// Button text with specific styling
  button,

  /// Link text with underline
  link,

  /// Numbers text with monospace font
  numbers,

  /// Secondary text color (f2)
  f2,

  /// Tertiary text color (f3)
  f3,
}
